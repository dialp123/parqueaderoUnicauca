    private void btnAsignarCupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarCupoActionPerformed
        // TODO add your handling code here:
        if(bahias != null){
            
            for(Vehiculo veh: cond.getVehiculos()){
                if (jComboBoxNumeroPlaca2.getSelectedItem().toString().equals(veh.getPlaca())){
                    for (Bahia bahia : bahias) {
                        if (jComboBoxBahias.getSelectedItem().toString().equals(Integer.toString(bahia.getNumero()))) {
                            JOptionPane.showMessageDialog(null, "Vehiculo con nro de placa: " + veh.getPlaca() + " asigando a la Bahia: " + bahia.getNumero());
                        }
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "No hay bahias disponibles para " + cond.getTipo());
        }
    }//GEN-LAST:event_btnAsignarCupoActionPerformed

