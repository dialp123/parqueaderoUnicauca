    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        
        cond = (Conductor) gestor.consultarConductor(txtNumeroDocumento.getText().toUpperCase(), 1);
        
        if (cond == null){
            Utilidades.mensajeAdvertencia("Usuario no resgitrado", "Atenci√≥n");
            txtNumeroDocumento.requestFocus();  
            txtNombres.setText("");
            txtApellidos.setText("");
            txtDocumento.setText("");
            txtTipoUsuario.setText("");
            txtMultas.setText("");
            jComboBoxNumeroPlaca.removeAllItems();
            jComboBoxNumeroPlaca2.removeAllItems();
            jComboBoxBahias.removeAllItems();
            txtTipo.setText("");
            txtNumeroPlaca.setText("");
            txtMarca.setText("");
            return;
        }
        
        bahias = new ArrayList();
        bahias =       (ArrayList<Bahia>) gestor.consultarConductor(txtNumeroDocumento.getText().toUpperCase(), 4);

        
        //informacion del ususario
        txtNombres.setText(cond.getNombres());
        txtApellidos.setText(cond.getApellidos());
        txtDocumento.setText(cond.getId());
        txtMultas.setText(""+cond.getMultas());
        txtTipoUsuario.setText(""+cond.getTipo());
        
        //agregar vehiculos al jcombox
        jComboBoxNumeroPlaca.removeAllItems();
        jComboBoxNumeroPlaca2.removeAllItems();
        for(Vehiculo veh: cond.getVehiculos()){
         jComboBoxNumeroPlaca.addItem(veh.getPlaca());  
         jComboBoxNumeroPlaca2.addItem(veh.getPlaca()); 
        }
        
        jComboBoxBahias.removeAllItems();
        if(bahias != null){
            for(int i=0; i<bahias.size(); i++){
                jComboBoxBahias.addItem(""+bahias.get(i).getNumero());   
            }   
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnMostrarInfoVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarInfoVehiculoActionPerformed
        // TODO add your handling code here:
        //informacion de vehiculos
        for(Vehiculo veh : cond.getVehiculos()) {
            if(jComboBoxNumeroPlaca.getSelectedItem().toString().equals(veh.getPlaca())){
                txtTipo.setText(veh.getTipo().toString());
                txtNumeroPlaca.setText(veh.getPlaca());
                txtMarca.setText(veh.getModelo());
            }
        }
    }//GEN-LAST:event_btnMostrarInfoVehiculoActionPerformed

